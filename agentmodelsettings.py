def composite_regional(component_weights, regional_raw, param_name, scale=1.0, offset=0.0):
    """
    Региональный профиль составного параметра через взвешенное среднее
    региональных профилей компонент. Регион включается только если
    присутствует хотя бы в 2 компонентах.
    """
    comp_names = list(component_weights.keys())
    all_regions = set()
    for n in comp_names:
        if n in regional_raw:
            all_regions.update(regional_raw[n].keys())

    result = {}
    for r in sorted(all_regions):
        comp_data = [(component_weights[n], regional_raw[n][r])
                     for n in comp_names
                     if n in regional_raw and r in regional_raw[n]]
        if len(comp_data) < 2:
            continue
        total_w = sum(w for w, _ in comp_data)
        w_mean  = sum(w * d['mean'] for w, d in comp_data) / total_w
        w_std   = sum(w * d['std']  for w, d in comp_data) / total_w
        min_n   = min(d['n'] for _, d in comp_data)
        result[r] = {"mean": sc(w_mean, scale, offset),
                     "std":  round(w_std * abs(scale), 4),
                     "n":    min_n}
    if result:
        top = sorted(result.items(), key=lambda x: x[1]['mean'], reverse=True)
        print(f"  {param_name}: " + " | ".join(f"{r}={v['mean']:.3f}(n={v['n']})" for r,v in top[:4]))
    return result

