def demographics(df, year_ref, year_col, edu_col, sex_col,
                 region_col=None, sett_col=None, sett_map=None, marital_col=None, label=""):
    birth = get_col(df, year_col)
    age   = (year_ref - birth) if birth.median() > 1900 else birth
    d = {
        'age':       age,
        'age_group': pd.cut(age, bins=[17,30,45,60,100], labels=["18-30","31-45","46-60","60+"]),
        'edu_group': get_col(df, edu_col).map(EDU),
        'sex':       get_col(df, sex_col).map({1:"male",2:"female"}),
    }
    if region_col:
        d['region']     = get_col(df, region_col).map(REGION)
    if sett_col and sett_map:
        d['settlement'] = get_col(df, sett_col).map(sett_map)
    if marital_col:
        d['marital']    = get_col(df, marital_col).map(MARITAL)
    demo = pd.DataFrame(d, index=df.index)
    print(f"  [{label}] возраст mean={demo['age'].mean():.1f} | "
          f"образование: {demo['edu_group'].value_counts(dropna=True).to_dict()}")
    return demo


