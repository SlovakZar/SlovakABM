def composite_from_variance(component_names, scores_dict, efa_variance, out_name):
    """
    Строит составной параметр как взвешенное среднее групповых средних.
    Веса = нормированная объяснённая дисперсия EFA каждого компонента.

    Поскольку компоненты из разных опросов (разные индексы), агрегация
    выполняется через групповые средние по (age_group, edu_group),
    а не через слияние индивидуальных наблюдений.

    Возвращает словарь {(age_group, edu_group): weighted_mean}
    для использования в group_stats на уровне агрегатов.
    """
    available = [(n, efa_variance[n]) for n in component_names
                 if n in scores_dict and n in efa_variance]
    if not available:
        print(f"  [{out_name}] ✗ компоненты не найдены")
        return None

    total_var = sum(v for _, v in available)
    weights   = {n: v / total_var for n, v in available}
    print(f"  [{out_name}] компоненты и веса (из дисперсии EFA):")
    for n, w in weights.items():
        print(f"    {n}: {efa_variance[n]:.1f}% → вес {w:.3f}")
    return weights


