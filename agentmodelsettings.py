def get_col(df, name, invert=False, mask=None):
    """
    Возвращает числовую серию столбца.
    invert=True: инвертирует шкалу через (max + min - x).
    mask: список значений → заменяются на NaN перед остальными операциями.
    """
    key = next((c for c in df.columns if c.upper() == name.upper()), None)
    if key is None:
        return pd.Series(np.nan, index=df.index, name=name)
    s = pd.to_numeric(df[key], errors='coerce').rename(name)
    if mask:
        s = s.where(~s.isin(mask), np.nan)
    if invert:
        vmax, vmin = s.max(), s.min()
        s = (vmax + vmin - s).rename(name)
    return s


def build_battery(df, spec):
    """
    Собирает батарею переменных и выводит диагностику.
    spec = {col_name: (invert_bool, mask_list_or_None)}
    Короткий синтаксис: {col_name: invert_bool} — mask = None.
    """
    result = {}
    for varname, opts in spec.items():
        if isinstance(opts, bool):
            inv, mask = opts, None
        else:
            inv, mask = opts
        s = get_col(df, varname, invert=inv, mask=mask)
        if s.notna().sum() > 0:
            result[varname] = s
            print(f"    {varname}: mean={s.mean():.2f}  NaN={s.isna().mean():.0%}"
                  + ("  [инверт]" if inv else "")
                  + (f"  [маска {mask}]" if mask else ""))
        else:
            print(f"    {varname}: ✗ нет данных")
    return pd.DataFrame(result, index=df.index)


def norm01(s):
    """
    Ранговая нормализация (процентильный ранг) → [0, 1].

    В отличие от MinMaxScaler, сохраняет форму распределения:
      - скошенность не растягивается
      - выбросы не задают крайние точки
      - доля агентов ниже порога приближена к реальной популяции

    Использует scipy.stats.rankdata — при недоступности scipy
    fallback на MinMaxScaler.
    """
    idx  = s.dropna().index
    vals = s.dropna().values
    if len(vals) < 2:
        return s
    try:
        from scipy.stats import rankdata
        ranks = rankdata(vals) / (len(vals) + 1)
    except ImportError:
        # Fallback: MinMax (старый метод)
        out = s.copy().astype(float)
        out[idx] = MinMaxScaler().fit_transform(vals.reshape(-1, 1)).flatten()
        return out
    out = s.copy().astype(float)
    out[idx] = ranks
    return out


