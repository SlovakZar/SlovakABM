def load_dataset(year: str):
    """
    Возвращает (df, codebook) или (None, None).
    codebook = {VAR_UPPER: {'scale': str, 'codes': {int: str}}}
    """
    import os
    path = FILES[year]
    if not os.path.exists(path):
        print(f"  [{year}] ✗ не найден: {path}")
        return None, None

    from openpyxl import load_workbook
    wb = load_workbook(path, read_only=True)

    ws = wb['Rawdata']
    all_rows  = list(ws.iter_rows(values_only=True))
    col_names = [str(v).strip() if v else f'_c{i}' for i, v in enumerate(all_rows[0])]
    data      = [list(r) for r in all_rows[2:]]
    df        = pd.DataFrame(data, columns=col_names[:len(data[0])])

    sentinel = SENTINEL_2022 if year == "2022" else SENTINEL_2024 if year == "2024" else SENTINEL_BASE
    for c in df.columns:
        s = pd.to_numeric(df[c], errors='coerce')
        df[c] = s.where(~s.isin(sentinel), np.nan)

    ws_lv    = wb['ListVariables']
    lv       = list(ws_lv.iter_rows(values_only=True))
    lv_names = [str(v).strip().upper() if v else '' for v in lv[0]]
    scale_row = lv[3] if len(lv) > 3 else []
    codebook  = {}
    for ci, varname in enumerate(lv_names):
        if not varname:
            continue
        scale = str(scale_row[ci]).strip() if ci < len(scale_row) and scale_row[ci] else 'unknown'
        codes = {}
        for ri in range(5, len(lv)):
            cell = lv[ri][ci] if ci < len(lv[ri]) else None
            if cell is None:
                continue
            m = re.match(r'^(-?\d+)\s*\|\s*(.+)$', str(cell).strip())
            if m:
                codes[int(m.group(1))] = m.group(2).strip()
        codebook[varname] = {'scale': scale, 'codes': codes}

    print(f"  [{year}] {df.shape[0]} строк × {df.shape[1]} столбцов  |  кодбук: {len(codebook)} перем.")
    return df, codebook


