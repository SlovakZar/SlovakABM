def run_efa(bat, name):
    """
    EFA (minres, 1 фактор) если Bartlett p<0.05 и KMO>0.5.
    Иначе — простое среднее. Выводит нагрузки и % объяснённой дисперсии.

    ИСПРАВЛЕНИЕ v7: после извлечения фактора проверяем знак нагрузок.
    Если большинство нагрузок отрицательные — инвертируем фактор.
    Это устраняет ситуацию когда высокий сырой балл (хорошие O36)
    давал низкий факторный балл после нормализации.
    """
    clean = bat.dropna()
    n, k  = clean.shape
    print(f"  [{name}] n={n}  k={k}")
    if n < 50 or k < 2:
        print(f"  [{name}] → мало данных, среднее")
        return bat.mean(axis=1).rename(name)
    try:
        _, p   = calculate_bartlett_sphericity(clean)
        _, kmo = calculate_kmo(clean)
    except Exception as e:
        print(f"  [{name}] ошибка теста: {e} → среднее")
        return bat.mean(axis=1).rename(name)
    ok = p < 0.05 and kmo > 0.5
    print(f"  [{name}] Bartlett p={p:.4f}{'✓' if p<0.05 else '✗'}  KMO={kmo:.3f}{'✓' if kmo>0.5 else '✗'}")
    if not ok:
        print(f"  [{name}] → EFA не рекомендован, среднее")
        return bat.mean(axis=1).rename(name)
    fa = FactorAnalyzer(n_factors=1, rotation=None, method='minres')
    fa.fit(clean)
    loadings = fa.loadings_[:, 0]

    # ── ИСПРАВЛЕНИЕ ЗНАКА ────────────────────────────────────────────────────
    # minres не гарантирует знак первого фактора.
    # Если большинство нагрузок отрицательные — весь фактор перевёрнут.
    # Инвертируем нагрузки и баллы чтобы высокий балл = высокое значение конструкта.
    n_negative = (loadings < 0).sum()
    sign = -1.0 if n_negative > k / 2 else 1.0
    if sign < 0:
        print(f"  [{name}] ⚠ знак инвертирован (большинство нагрузок < 0)")

    ld  = pd.DataFrame(loadings * sign, index=clean.columns, columns=["F1"])
    var = fa.get_factor_variance()[1][0]
    print(f"  [{name}] нагрузки (после коррекции знака):\n{ld.round(3).to_string()}")
    print(f"  [{name}] дисперсия: {var*100:.1f}%")

    raw_scores = fa.transform(clean)[:, 0] * sign
    out = pd.Series(np.nan, index=bat.index, name=name)
    out.update(pd.Series(raw_scores, index=clean.index))
    return out


def run_mca_svd(df, cols, name):
    """MCA через SVD для порядковых/номинальных переменных."""
    avail = [c for c in df.columns if c.upper() in [x.upper() for x in cols]]
    if not avail:
        return None
    sub = df[avail].apply(pd.to_numeric, errors='coerce').dropna().astype(int)
    if len(sub) < 50:
        return None
    dummies = pd.get_dummies(sub.astype(str)).astype(float)
    Z = dummies.values
    P = Z / Z.sum()
    r, c = P.sum(axis=1), P.sum(axis=0)
    S = (P - np.outer(r, c)) / np.outer(np.sqrt(r), np.sqrt(c))
    U, sv, _ = np.linalg.svd(S, full_matrices=False)
    print(f"  [{name}] MCA-SVD n={len(sub)}  sv1={sv[0]:.3f} sv2={sv[1]:.3f}")
    out = pd.Series(np.nan, index=df.index, name=name)
    out.update(pd.Series(U[:, 0], index=sub.index))
    return out


