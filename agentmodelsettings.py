def group_stats(score, demo, gcols, name):
    valid  = [c for c in gcols if demo is not None and c in demo.columns]
    if not valid:
        return {}, float(score.mean()), float(score.std())
    merged = pd.concat([score.rename('s'), demo[valid]], axis=1).dropna()
    gm, gs = float(merged['s'].mean()), float(merged['s'].std())
    print(f"  [{name}] global mean={gm:.3f} std={gs:.3f} n={len(merged)}")
    by_group = {}
    for gvals, sub in merged.groupby(valid, observed=True):
        if len(sub) < 8:
            continue
        key = str(gvals) if isinstance(gvals, tuple) else str((gvals,))
        by_group[key] = {
            "mean": round(float(sub['s'].mean()), 4),
            "std":  round(float(sub['s'].std()),  4),
            "p25":  round(float(sub['s'].quantile(.25)), 4),
            "p75":  round(float(sub['s'].quantile(.75)), 4),
            "n":    int(len(sub)),
        }
    return by_group, gm, gs


