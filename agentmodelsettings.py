def build_composite_group_stats(component_weights, model_params, out_name):
    """
    Вычисляет составной параметр через взвешенное среднее групповых средних.

    Для каждой демографической группы (age_group, edu_group):
      composite_mean = sum(weight_i * component_mean_i) / sum(weight_i_available)

    Стандартное отклонение вычисляется как взвешенное среднее SD компонент
    (консервативная оценка — без учёта ковариации между опросами).

    Группы включаются только если присутствуют хотя бы в 2 компонентах.
    """
    # Собираем все ключи групп из всех компонент
    all_keys = set()
    for name in component_weights:
        if name in model_params:
            all_keys.update(model_params[name]['by_group'].keys())

    by_group = {}
    weight_vals = list(component_weights.values())
    comp_names  = list(component_weights.keys())

    for key in sorted(all_keys):
        comp_data = [(component_weights[n], model_params[n]['by_group'][key])
                     for n in comp_names
                     if n in model_params and key in model_params[n]['by_group']]
        if len(comp_data) < 2:
            continue
        total_w = sum(w for w, _ in comp_data)
        w_mean  = sum(w * d['mean'] for w, d in comp_data) / total_w
        w_std   = sum(w * d['std']  for w, d in comp_data) / total_w
        min_n   = min(d['n'] for _, d in comp_data)
        by_group[key] = {
            "mean": round(w_mean, 4),
            "std":  round(w_std,  4),
            "p25":  round(w_mean - 0.674 * w_std, 4),  # приближение нормального
            "p75":  round(w_mean + 0.674 * w_std, 4),
            "n":    min_n,
            "n_components": len(comp_data),
        }

    if not by_group:
        print(f"  [{out_name}] ✗ нет групп с достаточным перекрытием")
        return None

    gm = sum(v['mean'] for v in by_group.values()) / len(by_group)
    gs = sum(v['std']  for v in by_group.values()) / len(by_group)
    print(f"  [{out_name}] global mean={gm:.3f}  std={gs:.3f}  groups={len(by_group)}")

    return {"global_mean": round(gm, 4),
            "global_std":  round(gs, 4),
            "by_group":    by_group,
            "composite_weights": {k: round(v, 4) for k, v in component_weights.items()}}


